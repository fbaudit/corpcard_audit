const holidaySet = new Set<string>(KR_HOLIDAYS);

for (const extra of (process.env.CORPCARD_EXTRA_HOLIDAYS ?? "").split(",")) {
  const trimmed = extra.trim();
  if (/^\d{4}-\d{2}-\d{2}$/.test(trimmed)) holidaySet.add(trimmed);
}

export function isKoreanHoliday(isoDate: string): boolean {
  return holidaySet.has(isoDate);
}
